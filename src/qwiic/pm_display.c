void pm_display_prevScreen()
{
   switch(pm_display.prev_screen)
      {
       case D_GMIDI:
            pm_display_gmidi();
            break;
       case D_SB:
            pm_display_sb();
            break;
       case D_ADLIB:
            pm_display_adlib();
            break;
       case D_GUS:
            pm_display_gus();
            break;
       case D_PMHOME:         
            pm_display_pmhome();
            break;
       case D_FLOPPY_S:
          break;          
          }  
}

void pm_display_contrast(uint8_t contrast)
{
  if (contrast!=pm_display.contrast)
  {
   oledSet_contrast(&dev_oled_screen, contrast);
   pm_display.contrast=contrast;
  }
}

void pm_display_event(uint8_t ScreenID, uint8_t code1, uint8_t code2, char *event_msg)
{
  switch(ScreenID)
      {
       case D_PMPOST:
            pm_display_pmpost_event(code1);
            pm_display.temporary_screen=true;
            pm_display.temporary_delay=60;   // (6s before going back to previous screen)
            break;
       case D_PCPOST:
            pm_display_pcpost_event(code1,code2);
            pm_display.temporary_screen=true;
            pm_display.temporary_delay=60;   // (6s before going back to previous screen)            
            break;
       case D_PAD:
            pm_display_pad_event(code1,event_msg);
            pm_display.temporary_screen=true;
            pm_display.temporary_delay=60;   // (6s before going back to previous screen)            
            break;
       case D_GMIDI:
       case D_SB:
       case D_ADLIB:
       case D_GUS:
       case D_FLOPPY_S:
       case D_INFO_S:
       break;          
      }  
}

void pm_display_worker(uint32_t ms_since_last)
{
  static uint32_t pm_display_dev_active =0;

  if (!dev_oled_screen.enabled) return;
  if (pm_display.hc_delay--==0) pm_display_contrast(CONTRAST_LOW);

  if (dev_audiomix.dev_active!=pm_display_dev_active) {
   pm_display_dev_active=dev_audiomix.dev_active;
   if (pm_display_dev_active) {      
      pm_display.hc_delay=50;   // (5s before going low)
      PM_INFO("Audio Device Active : %04X\n",dev_audiomix.dev_active);
      if (dev_audiomix.dev_active & AD_ADLIB) {
         PM_INFO("AdLib detected\n");
         pm_display_adlib();      
      } else if (dev_audiomix.dev_active & AD_MPU) {
         PM_INFO("General MIDI detected\n");        
         pm_display_gmidi();
      }  else if (dev_audiomix.dev_active & AD_GUS) {
         PM_INFO("Gravis Ultrasound detected\n");
         pm_display_gus();
      } else if (dev_audiomix.dev_active & AD_SBDSP) {
         PM_INFO("Sound Blaster detected\n");
         pm_display_sb();
        }
      }
   } else {
     //Go back to the previous Display Screen if no more audio device active
   }

   // display the previous screen after temporary screen delay
   if (pm_display.temporary_screen)
      {
        if (pm_display.temporary_delay--==0)
          {
          pm_display_prevScreen();
          pm_display.temporary_screen=false;
         }
      }

   // Screen needing regular refresh
   switch(pm_display.screen)
      {
       case D_GMIDI:
            pm_display_gmidi();
            break;
       case D_SB:
            pm_display_sb();
            break;
       case D_ADLIB:
            pm_display_adlib();
            break;
       case D_GUS:
            pm_display_gus();
            break;
          break;
      }
}