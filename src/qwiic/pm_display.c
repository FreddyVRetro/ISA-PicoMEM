void oled_init(t_OledParams *oled,uint8_t oled_type) {
   if (oled_type==OLED_SH1106_64)
      {
       PM_INFO("OLED : SH1106 132x64\n");            
       oled->ctlrType    = CTRL_SH1106; // CTRL_SSD1306
       oled->height      = H_64;
       oled->width       = W_132; //W_128
      } 
       else if (oled_type==OLED_SSD1306_32)
      {
       PM_INFO("OLED : SSD1306 128x32\n");
       oled->ctlrType    = CTRL_SSD1306;
       oled->height      = H_32;
       oled->width       = W_128;
      } else if (oled_type==OLED_SSD1306_64)
      {
       PM_INFO("OLED : SSD1306 128x64\n");
       oled->ctlrType    = CTRL_SSD1306;
       oled->height      = H_64;
       oled->width       = W_128;
      } else return;
   oled->i2c         = FP_I2C;
   oled->SDA_PIN     = FP_SDA;
   oled->SCL_PIN     = FP_SCL;

// Initialize the I2C I/O
   i2c_init(oled->i2c, 200000);
//    i2c_init(oled->i2c, 400000);
   gpio_init(oled->SDA_PIN);
   gpio_init(oled->SCL_PIN);
   gpio_set_function(oled->SDA_PIN, GPIO_FUNC_I2C);
   gpio_set_function(oled->SCL_PIN, GPIO_FUNC_I2C);
   gpio_pull_up(oled->SDA_PIN);
   gpio_pull_up(oled->SCL_PIN);

   PM_INFO("OLED I2C configured on pins %d (SDA) and %d (SCL)\n", oled->SDA_PIN, oled->SCL_PIN); 
   oled->enabled = false;

   if (pico_i2c_ping_device(oled->i2c, 0x3C))
     {  
      oled->i2c_address = 0x3C;
      oled->enabled     = true;
      PM_INFO("Start OLED : %d, h%d w%d\n", oled->ctlrType,oled->height,oled->width);
      // this will configure the OLED module and then clear the screen.
      oledI2cConfig(oled);
     }
}


