/*
Ą – 0x0104; ą – 0x0105 > altGr with key with base letter ("Caps Lock" respected) altGr + key "a" produced "ą" shift + altGr + key "a" produced "Ą"
Ć - 0x0106; ć – 0x0107 > as previous
Ę - 0x0118; ę – 0x0119 > as previous
Ł - 0x0141; ł – 0x0142 > as previous
Ń - 0x0143; ń – 0x0144 > as previous
Ó - 0x00d3; ó – 0x00f3 > as previous
Ś - 0x015a; ś – 0x015b > as previous
Ż - 0x017b; ż – 0x017c > altGr with key with base letter here (respect state of "Caps Lock") altGr + key "z" produced "ż" shift + altGr + key "z" produced "Ż"
exception due to second letter in polish alphabet based on "z":
Ź - 0x0179; ź – 0x017a > altGr with key with base letter here (respect state of "Caps Lock") altGr + key "x" produced "ź" shift + altGr + key "x" produced "Ź"

additional signs :
§ - 0x00a7 altGr with key "3"
€ - 0x20ac altGr with key "4" 
*/

